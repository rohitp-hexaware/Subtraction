package com.rohit;
/**
 *This is java class for subtraction.
 */
public class subtraction {
/**
 *This is method for subtraction.
 *@param a is first parameter to subtraction.
 *@param b is second parameter to subtraction.
 *@return  is returning values of subtraction.
 */
    public final int subtract(final int a, final int b) {
        return (a - b);
    }
}
